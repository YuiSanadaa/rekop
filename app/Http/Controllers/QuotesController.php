     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $r)
    {
        $add = new Quotes;
        $add->nama = $r->input('nama');
        $add->quotes = $r->input('quotes');
        $add->Save();
        return back();

    }

